package extra;

public class Dowhileloop {

	public static void main(String[] arguments) {

        // In this program we will print the numbers from 100 to 81 using do while loop.

        int i = 100;
        do {
            System.out.println(i);

            // Decrement the value of i by 1
            i--;
        } while (i >= 81); // Checking the condition for number.
    }
	
}
