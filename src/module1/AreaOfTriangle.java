package module1;

public class AreaOfTriangle {
	public static void main(String[] args) {
		
		// Let's take base and height of triangle
		int base = 5;
		int height = 10;
		
		// Area Operation
		double area = 0.5 * base * height;
		
		// Printing Area
		System.out.println("Area of Triangle is => " + area);
		
	}

}

