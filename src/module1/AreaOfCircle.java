package module1;

public class AreaOfCircle {
	public static void main(String[] args) {
		
		// Initilise Value
		int radius = 6;
		
		// Area of Circle Operation | double used because of PI values.
		double area = Math.PI * radius * radius;
		
		// Printing Area
		System.out.println("Area of Circle is => " + area);
		
	}

}
